public void MostrarDados(){
    try {
        con_preco.executarSQL("select * from preco");
        con_preco.resultSet.first();
        cod.setText(con_preco.resultSet.getString("cod"));
        vhgrande.setText(con_preco.resultSet.getString("vhoravgrande"));
        vhmedio.setText(con_preco.resultSet.getString("vhoravmedio"));
        vhpequeno.setText(con_preco.resultSet.getString("vhoravpequeno"));
        vmesgrande.setText(con_preco.resultSet.getString("valormesvgrande"));
        vmesmedio.setText(con_preco.resultSet.getString("valormesvmedio"));
        vmespequeno.setText(con_preco.resultSet.getString("valormesvpequeno"));
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Não foi possivel localizar os Dados");
    }
}

public void AlteraPreco(){
    try {
        con_preco.executarSQL("select * from preco");
        String sql = "UPDATE preco SET"
                + " vhoravgrande = '" + vhgrande.getText() + "',"
                + " vhoravmedio = '" + vhmedio.getText() + "',"
                + " vhoravpequeno = '" + vhpequeno.getText() + "',"
                + " valormesvgrande = '" + vmesgrande.getText() + "',"
                + " valormesvmedio = '" + vmesmedio.getText() + "',"
                + " valormesvpequeno = '" + vmespequeno.getText() + "'"
                + " where cod = " + cod.getText();
        con_preco.statement.executeUpdate(sql);
        JOptionPane.showMessageDialog(null, "Alteração realizada com sucesso");
        con_preco.executarSQL("select * from preco");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro na Alteração" + e);
        
    }
}

